### This is for users who are using developer environment created by admins.
### Run this from the any client (Linux, Mac, Windows) that has BASH installed.
### You should use your local computer to run this, as it will create SSH Tunnel between your computer and remote Compute Instance.
### While the SSH Tunnel is created, you can open your local browser and go to https://localhost:8000/ to access Jupyter from remote machine.
### When you're done with your work, you can close the SSH Tunnel.

